package com.tcl.dias.beans;


public class ConferenceRoomUsageBean {
	private String customerName;
	private String month;
	private String year;
	private String monthShortName;
    private String roomName;
    private String conferenceCount;
    private String conferenceMinutes;
    private String conferenceDurationAvg;
    private String createdAt;
    private String updatedAt;
    
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getMonthShortName() {
		return monthShortName;
	}
	public void setMonthShortName(String monthShortName) {
		this.monthShortName = monthShortName;
	}
	public String getRoomName() {
		return roomName;
	}
	public void setRoomName(String roomName) {
		this.roomName = roomName;
	}
	public String getConferenceCount() {
		return conferenceCount;
	}
	public void setConferenceCount(String conferenceCount) {
		this.conferenceCount = conferenceCount;
	}
	public String getConferenceMinutes() {
		return conferenceMinutes;
	}
	public void setConferenceMinutes(String conferenceMinutes) {
		this.conferenceMinutes = conferenceMinutes;
	}
	public String getConferenceDurationAvg() {
		return conferenceDurationAvg;
	}
	public void setConferenceDurationAvg(String conferenceDurationAvg) {
		this.conferenceDurationAvg = conferenceDurationAvg;
	}
	public String getCreatedAt() {
		return createdAt;
	}
	public void setCreatedAt(String createdAt) {
		this.createdAt = createdAt;
	}
	public String getUpdatedAt() {
		return updatedAt;
	}
	public void setUpdatedAt(String updatedAt) {
		this.updatedAt = updatedAt;
	}
    

}
