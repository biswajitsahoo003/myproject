public class BandwidthSummaryDetails {

	private String sitetype;

	private Integer upgradedCount;

	private Integer retainedCount;

	public String getSitetype() {
		return sitetype;
	}

	public void setSitetype(String sitetype) {
		this.sitetype = sitetype;
	}

	public Integer getUpgradedCount() {
		return upgradedCount;
	}

	public void setUpgradedCount(Integer upgradedCount) {
		this.upgradedCount = upgradedCount;
	}

	public Integer getRetainedCount() {
		return retainedCount;
	}

	public void setRetainedCount(Integer retainedCount) {
		this.retainedCount = retainedCount;
	}

	
}
