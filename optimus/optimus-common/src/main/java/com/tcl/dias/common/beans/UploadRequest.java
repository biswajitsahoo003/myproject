package com.tcl.dias.common.beans;

public class UploadRequest {
	
	private Integer customerLeId;
	private String referenceName;
	private String attachmentType;
	private String productName;
	private String newFolder;
	private Long newPath;
	/**
	 * @return the customerLeId
	 */
	public Integer getCustomerLeId() {
		return customerLeId;
	}
	/**
	 * @param customerLeId the customerLeId to set
	 */
	public void setCustomerLeId(Integer customerLeId) {
		this.customerLeId = customerLeId;
	}
	/**
	 * @return the referenceName
	 */
	public String getReferenceName() {
		return referenceName;
	}
	/**
	 * @param referenceName the referenceName to set
	 */
	public void setReferenceName(String referenceName) {
		this.referenceName = referenceName;
	}
	/**
	 * @return the attachmentType
	 */
	public String getAttachmentType() {
		return attachmentType;
	}
	/**
	 * @param attachmentType the attachmentType to set
	 */
	public void setAttachmentType(String attachmentType) {
		this.attachmentType = attachmentType;
	}
	/**
	 * @return the productName
	 */
	public String getProductName() {
		return productName;
	}
	/**
	 * @param productName the productName to set
	 */
	public void setProductName(String productName) {
		this.productName = productName;
	}
	/**
	 * @return the newFolder
	 */
	public String getNewFolder() {
		return newFolder;
	}
	/**
	 * @param newFolder the newFolder to set
	 */
	public void setNewFolder(String newFolder) {
		this.newFolder = newFolder;
	}
	/**
	 * @return the newPath
	 */
	public Long getNewPath() {
		return newPath;
	}
	/**
	 * @param newPath the newPath to set
	 */
	public void setNewPath(Long newPath) {
		this.newPath = newPath;
	}

	
	

}
